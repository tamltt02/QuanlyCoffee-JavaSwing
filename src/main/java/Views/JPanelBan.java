    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        // TODO add your handling code here:
        int choose = JOptionPane.showConfirmDialog(this, "Xác nhận!");
        if (choose == 0) {
            _ServiceBan.create(getGUI(1));
            loadTable(_ServiceBan.getlst());
            JOptionPane.showMessageDialog(this, "Thêm thành công!");
            return;
        }
        JOptionPane.showMessageDialog(this, "Thêm thất bại!");

    }//GEN-LAST:event_btn_ThemActionPerformed

    private void btn_SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaActionPerformed
        // TODO add your handling code here:
        if (tbl_Ban.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Bạn phải chọn bàn");
            return;
        }

        int choose = JOptionPane.showConfirmDialog(this, "Xác nhận!");
        if (choose == 0) {
            _ServiceBan.update(getGUI(0));
            loadTable(_ServiceBan.getlst());
            JOptionPane.showMessageDialog(this, "Sửa thành công!");
            return;
        }
        JOptionPane.showMessageDialog(this, "Sửa thất bại!");
    }//GEN-LAST:event_btn_SuaActionPerformed

