    void trangThai() {
        cbx_TrangThai.removeAllItems();
        cbx_TrangThai.addItem("Trống");
        cbx_TrangThai.addItem("Có khách");
        cbx_TrangThai.addItem("Không sử dụng");
    }

    void loadTable(List<Ban> lst) {
        _DefaultTableModel = (DefaultTableModel) tbl_Ban.getModel();
        if (lst.isEmpty()) {
            _DefaultTableModel.setRowCount(0);
            return;
        }
        
        _DefaultTableModel.setRowCount(0);
        for (Ban x : lst) {
            _DefaultTableModel.addRow(new Object[]{
                x.getMaBan(), x.getTrangThai() == 0 ? "Trống" : x.getTrangThai() == 1 ? "Có khách" : "Không sử dụng", x.getSoGhe()
            });
        }
    }

    BanView getGUI(int style) {
        int ma = _daoBan.findAll().get(_daoBan.findAll().size()-1).getID_Ban();
        if (style == 1) {
            return new BanView(_getid.getIDMax("B", ma), cbx_TrangThai.getSelectedItem() == "Trống" ? 0 : cbx_TrangThai.getSelectedItem() == "Trống" ? 1 : 2, Integer.parseInt(txt_SoGhe.getText()));
        }
        return new BanView(_ServiceBan.getlst().get(tbl_Ban.getSelectedRow()).getID_Ban(), txt_MaBan.getText(), cbx_TrangThai.getSelectedItem() == "Trống" ? 0 : cbx_TrangThai.getSelectedItem() == "Trống" ? 1 : 2, Integer.parseInt(txt_SoGhe.getText()));
    }

