    void loaddatasp(List<thongkeSanPham> list) {

        _tablsp = (DefaultTableModel) tblsp.getModel();
        _tablsp.setRowCount(0);
        int stt = 1;
        for (thongkeSanPham s : list) {
            _tablsp.addRow(new Object[]{stt++, s.getMaSp(), s.getTensp(), s.getGiaban(), s.getSoluong()});
        }
    }

    void loaddatacb(List<thongkeCombo> list) {
        _tablcb = (DefaultTableModel) tblcb.getModel();
        _tablcb.setRowCount(0);
        int stt = 1;
        for (thongkeCombo s : list) {
            _tablcb.addRow(new Object[]{stt++, s.getmaCombo(), s.gettencombo(), s.getGiaban(), s.getSoluong()});
        }
    }

